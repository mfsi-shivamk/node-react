export default function Header() {
  const classes = useStyles();
  const theme = useTheme();
  const history = useHistory();
  const [open, setOpen] = React.useState(true);

  const routeChange = (path) =>{  
    history.push(path);
    window.location.href = path;
  }
  const handleDrawerOpen = () => {
    setOpen(true);
  };

  const handleDrawerClose = () => {
    setOpen(false);
  };

  return (
    <div className={classes.root}>
      <CssBaseline />
      <AppBar
        color="inherit"
        style={{boxShadow: "none"}}
        position="fixed"
        className={clsx(classes.appBar, {
          [classes.appBarShift]: open,
        })}
      >
        <Toolbar>
          <IconButton
            color="inherit"
            aria-label="open drawer"
            onClick={handleDrawerOpen}
            edge="start"
            className={clsx(classes.menuButton, {
              [classes.hide]: open,
            })}
          >
            <Typography variant="h6" noWrap>
            {(localStorage.user && localStorage.user.length)  ? localStorage.user[0].toUpperCase():"A"}
          </Typography>
          </IconButton>
            <MenuIcon onClick={open? handleDrawerClose : handleDrawerOpen}/>
            <div className={classes.search}>
            <div className={classes.searchIcon}>
              <SearchIcon />
            </div>
            <InputBase
              placeholder="Searchâ€¦"
              classes={{
                root: classes.inputRoot,
                input: classes.inputInput,
              }}
              inputProps={{ 'aria-label': 'search' }}
            />
          </div>
        </Toolbar>
      </AppBar>
      <Drawer
        style={{  opacity: 0.6 }}
        variant="permanent"
        className={clsx(classes.drawer, {
          [classes.drawerOpen]: open,
          [classes.drawerClose]: !open,
        })}
        classes={{
          paper: clsx({
            [classes.drawerOpen]: open,
            [classes.drawerClose]: !open,
          }),
        }}
      >
        <div className={classes.toolbar} style={{float:'left'}}>
          <IconButton onClick={handleDrawerClose}>
          <Typography variant="h6" noWrap>
            {(localStorage.user && localStorage.user.length)  ? localStorage.user.toUpperCase():"A"}
          </Typography>          
          </IconButton>
        </div>
        <Divider />
        <List>
          {[{text:'movie', link:'movie'}, {text:'Eye Vision Screening', link:'eye'},{text:'Eye Screening Results', link:'eye-test'},{text:'logout', link:'logout'}]
          .map(({text,link}, index) => (
            <ListItem button key={text.toUpperCase()} onClick={()=>routeChange(link)}>
              <ListItemIcon>{index % 2 === 0 ? <InboxIcon /> : <MailIcon />}</ListItemIcon>
              <ListItemText primary={text[0].toUpperCase()+text.slice(1)} />
            </ListItem>
          ))}
        </List>
        <Divider />
      </Drawer>
    </div>
  );
}